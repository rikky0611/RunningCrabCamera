import UIKit

extension UIColor {
    static func crabRed() -> UIColor {
        return UIColor(red: 238/255, green: 95/255, blue: 81/255, alpha: 1)
    }
    
    static func crabBlue() -> UIColor {
        return UIColor(red: 131/255, green: 199/255, blue: 238/255, alpha: 1)
    }
}

extension UIColor {
    static func facebookColor() -> UIColor {
        return UIColor(red: 59/255, green: 87/255, blue: 157/255, alpha: 1)
    }
    
    static func twitterColor() -> UIColor {
        return UIColor(red: 85/255, green: 172/255, blue: 238/255, alpha: 1)
    }
}